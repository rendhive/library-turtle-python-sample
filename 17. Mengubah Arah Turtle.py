import turtle

def change_direction():
    turtle.forward(100)
    turtle.left(45)  # Belok kiri 45 derajat
    turtle.forward(100)
    turtle.right(90)  # Belok kanan 90 derajat
    turtle.forward(100)
    turtle.done()

change_direction()
# Fungsi: Mengubah arah gerakan turtle dengan sudut yang berbeda.
# Kondisi: Ketika Anda ingin menyesuaikan arah bergerak.