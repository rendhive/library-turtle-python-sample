import turtle

def draw_background():
    turtle.bgcolor("lightblue")  # Mengatur warna latar belakang
    turtle.color("orange")
    turtle.circle(50)
    turtle.done()

draw_background()
# Fungsi: Mengatur warna latar belakang sebelum menggambar.
# Kondisi: Ketika Anda ingin meningkatkan tampilan antarmuka grafis.