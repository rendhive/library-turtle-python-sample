import turtle

def turtle_basics():
    turtle.forward(100)  # Maju 100 unit
    turtle.right(90)     # Belok kanan 90 derajat
    turtle.forward(100)  # Maju lagi 100 unit
    turtle.done()

turtle_basics()
# Fungsi: Menunjukkan dasar dasar pergerakan turtle.
