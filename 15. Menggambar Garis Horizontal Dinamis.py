import turtle

def draw_dynamic_line():
    turtle.goto(-100, 0)  # Memindahkan turtle ke lokasi tertentu
    turtle.goto(100, 0)   # Gambar garis horizontal
    turtle.done()

draw_dynamic_line()
# Fungsi: Menggambar garis dari titik ke titik menggunakan koordinat.
# Kondisi: Ketika Anda ingin menggambar garis pada posisi yang ditentukan.